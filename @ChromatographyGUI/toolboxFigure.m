function toolboxFigure(obj, varargin)

% ---------------------------------------
% Figure
% ---------------------------------------
obj.figure = figure(...
    'name',                [obj.name, ' (', obj.version, ')'],...
    'tag',                 'toolboxfigure',...
    'numbertitle',         'off',...
    'menubar',             'none',...
    'toolbar',             'none',...
    'dockcontrols',        'on',...
    'visible',             'off',...
    'resize',              'on',...
    'selected',            'off',...
    'selectionhighlight',  'off',...
    'units',               'normalized',...
    'color',               [1.00, 1.00, 1.00],...
    'position',            [0.050, 0.125, 0.900, 0.800],...
    'sizechangedfcn',      '',...
    'resizefcn',           '');

end